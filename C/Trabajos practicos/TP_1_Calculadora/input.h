/** \brief Solicita ingresar un numero entero, y lo retorna.
 *
 * \param mensaje[] char Mensaje a ser mostrado cuando se solicita ingresar un numero.
 * \return int Retorna el numero entero ingresado.
 *
 */
int getInt(char mensaje[]);

/** \brief Solicita ingresar un numero flotante y lo retorna.
 *
 * \param mensaje[] char Mensaje a ser mostrado cuando se solicita ingresar un numero.
 * \return float Retorna el numero flotante ingresado.
 *
 */
float getFloat(char mensaje[]);

/** \brief Solicita ingresar un caracter, y lo retorna.
 *
 * \param mensaje[] char Mensaje a ser mostrado cuando se solicita ingresar un caracter.
 * \return char Devuelve el caracter ingresado.
 *
 */
char getChar(char mensaje[]);
